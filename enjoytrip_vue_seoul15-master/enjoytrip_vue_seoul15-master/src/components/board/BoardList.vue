<script setup>
import { ref, onMounted } from "vue";
import { useRouter } from "vue-router";
import { listArticle } from "@/api/board.js";
import "../../assets/scss/2_pages/BoardView.scss";

import VSelect from "@/components/common/VSelect.vue";
import BoardListItem from "@/components/board/item/BoardListItem.vue";
import PageNavigation from "@/components/common/PageNavigation.vue";

const router = useRouter();

const selectOption = ref([
  { text: "검색조건", value: "" },
  { text: "글번호", value: "article_no" },
  { text: "제목", value: "subject" },
  { text: "작성자아이디", value: "user_id" },
]);

const articles = ref([]);
const currentPage = ref(1);
const totalPage = ref(0);
const { VITE_ARTICLE_LIST_SIZE } = import.meta.env;
const param = ref({
  pgno: currentPage.value,
  spp: VITE_ARTICLE_LIST_SIZE,
  key: "",
  word: "",
});

onMounted(() => {
  getArticleList();
});

const changeKey = (val) => {
  console.log("BoarList에서 선택한 조건 : " + val);
  param.value.key = val;
};

const getArticleList = () => {
  console.log("서버에서 글목록 얻어오자!!!", param.value);
  listArticle(
    param.value,
    ({ data }) => {
      articles.value = data.articles;
      currentPage.value = data.currentPage;
      totalPage.value = data.totalPageCount;
    },
    (error) => {
      console.log(error);
    }
  );
};

const onPageChange = (val) => {
  console.log(val + "번 페이지로 이동 준비 끝!!!");
  currentPage.value = val;
  param.value.pgno = val;
  getArticleList();
};

const moveWrite = () => {
  router.push({ name: "article-write" });
};
</script>

<template>
  <div class="container">
    <div class="boardlist-wrap">
      <div class="boardlist-title-wrap">
        <h2>글목록</h2>
      </div>

      <div class="boardlist-content-wrap">
        <div class="boardlist-search-wrap">
          <div class="search-form-wrap">
            <form>
              <VSelect :selectOption="selectOption" @onKeySelect="changeKey" />
              <div class="">
                <input
                  type="text"
                  class="form-control radius25"
                  v-model="param.word"
                  placeholder="검색어..."
                />
                <button
                  class="search-post btn btn-main radius25"
                  type="button"
                  @click="getArticleList"
                >
                  <span class="material-symbols-outlined"> search </span>
                  <p>검색</p>
                </button>
              </div>
            </form>
          </div>
          <div class="write-btn-wrap">
            <button type="button" class="write-post btn btn-main radius25" @click="moveWrite">
              <span class="material-symbols-outlined"> edit </span>
              <p>POST</p>
            </button>
          </div>
        </div>

        <BoardListItem
          v-for="article in articles"
          :key="article.articleNo"
          :article="article"
          @get-board-list="getArticleList"
        ></BoardListItem>
      </div>

      <PageNavigation
        :current-page="currentPage"
        :total-page="totalPage"
        @pageChange="onPageChange"
      ></PageNavigation>
    </div>
  </div>
</template>

<style scoped>
.container {
  padding: 60px 0;
}
</style>
